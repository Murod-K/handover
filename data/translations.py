def t(key: str, lang: str, **kwargs) -> str:
    """Get translated string for given key and language."""
    entry = T.get(key, {})
    text = entry.get(lang) or entry.get("ru") or key
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, ValueError):
            pass
    return text
